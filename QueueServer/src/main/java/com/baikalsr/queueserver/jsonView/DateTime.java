package com.baikalsr.queueserver.jsonView;

public class DateTime {
    private String Date;
    private String Time;

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }

    public String getTime() {
        return Time;
    }

    public void setTime(String time) {
        Time = time;
    }
}
