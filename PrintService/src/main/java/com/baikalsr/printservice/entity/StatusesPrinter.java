package com.baikalsr.printservice.entity;

public class StatusesPrinter {
    private String statusPrinter;
    private String statusPaper;
    private String statusCuter;
    private String statusHead;

    public String getStatusPrinter() {
        return statusPrinter;
    }

    public void setStatusPrinter(String statusPrinter) {
        this.statusPrinter = statusPrinter;
    }

    public String getStatusPaper() {
        return statusPaper;
    }

    public void setStatusPaper(String statusPaper) {
        this.statusPaper = statusPaper;
    }

    public String getStatusCuter() {
        return statusCuter;
    }

    public void setStatusCuter(String statusCuter) {
        this.statusCuter = statusCuter;
    }

    public String getStatusHead() {
        return statusHead;
    }

    public void setStatusHead(String statusHead) {
        this.statusHead = statusHead;
    }
}
